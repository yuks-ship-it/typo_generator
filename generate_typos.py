# ------------------------------
# Helper functions to generate typos
# ------------------------------

def get_char_set(word_list):
    """Return a set of all characters used in the given word list."""
    chars = set()
    for w in word_list:
        chars.update(w)
    return chars

def random_insert(word, char_set):
    pos = random.randint(0, len(word))
    ch = random.choice(list(char_set))
    return word[:pos] + ch + word[pos:]

def random_delete(word):
    if len(word) <= 1:
        return word  # cannot delete
    pos = random.randint(0, len(word)-1)
    return word[:pos] + word[pos+1:]

def random_substitute(word, char_set):
    if len(word) == 0:
        return word
    pos = random.randint(0, len(word)-1)
    ch = random.choice(list(char_set))
    # avoid substituting with the same character (optional)
    while ch == word[pos] and len(char_set) > 1:
        ch = random.choice(list(char_set))
    return word[:pos] + ch + word[pos+1:]

def random_swap(word):
    if len(word) < 2:
        return word
    pos = random.randint(0, len(word)-2)
    # if swapped chars are same, no change; we can retry or just return
    if word[pos] == word[pos+1]:
        return word
    return word[:pos] + word[pos+1] + word[pos] + word[pos+2:]

def generate_typo(word, char_set):
    """Generate a random typo for the given word using one of four operations."""
    # Each operation is stored with a flag indicating whether it needs char_set
    operations = [
        (random_insert, True),
        (random_delete, False),
        (random_substitute, True),
        (random_swap, False)
    ]
    valid_ops = []
    for op, needs_char in operations:
        if op == random_delete and len(word) <= 1:
            continue
        if op == random_swap and len(word) < 2:
            continue
        valid_ops.append((op, needs_char))

    if not valid_ops:
        return word  # fallback

    op, needs_char = random.choice(valid_ops)
    if needs_char:
        typo = op(word, char_set)
    else:
        typo = op(word)

    # Ensure typo is different from original (simple retry up to 3 times)
    attempts = 0
    while typo == word and attempts < 3:
        op, needs_char = random.choice(valid_ops)
        if needs_char:
            typo = op(word, char_set)
        else:
            typo = op(word)
        attempts += 1

    return typo

# ------------------------------
# Generate output files
# ------------------------------

def generate_file(lang_name, words):
    char_set = get_char_set(words)
    filename = f"{lang_name}_typos.txt"
    with open(filename, "w", encoding="utf-8") as f:
        for w in words:
            # Write correct line
            f.write(f"{w},{w}\n")
            # Generate and write typo line
            typo = generate_typo(w, char_set)
            f.write(f"{typo},{w}\n")
    print(f"Generated {filename} with {len(words)*2} lines.")

